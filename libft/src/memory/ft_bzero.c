#include "../../includes/libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*str;

	str = (char *)s;
	if (n == 0)
		return ;
	i = -1;
	while (++i < n)
		str[i] = 0;
	return ;
}
