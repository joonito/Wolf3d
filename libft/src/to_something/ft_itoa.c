#include "../../includes/libft.h"

char	*ft_itoa(int nbr)
{
	char	s[13];
	int		i;
	int		sign;

	i = 12;
	sign = 1;
	s[i--] = '\0';
	if (nbr < 0)
		sign = -1;
	if (nbr == 0)
		s[i--] = '0';
	while (nbr)
	{
		s[i--] = (sign == 1) ? nbr % 10 + '0' : -(nbr % 10) + '0';
		nbr /= 10;
	}
	if (sign == -1)
		s[i--] = '-';
	return (ft_strdup(&s[++i]));
}
